public class DomainTest {
    private int a;
    private String b;

    public DomainTest(int a, String b) {
        this.a = a;
        this.b = b;
    }

    public DomainTest() {
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }
}
